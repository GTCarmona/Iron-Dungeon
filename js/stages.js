
class Room {
  constructor(roomNum) {
    this.map = roomNum
    this.tileSize = 16
    
  }
  draw(ctx) {
    for (var row = 0;row < this.map.length; row++) {
      for (var col = 0; col < this.map[row].length; col++) {
        if (this.map[row][col] === 1) {
          let sx = 16 + 16 * Math.floor((row**2+col)%3) // floor tiles
          let sy = 64
          ctx.drawImage(spriteImg, sx, sy, 16, 16, col*this.tileSize, row*this.tileSize, this.tileSize, this.tileSize)
        }
        else if (this.map[row][col] === 2) {
          let sx = 16 +16 * Math.floor((row**2+col)%2) // wall front
          let sy = 16
          ctx.drawImage(spriteImg, sx, sy, 16, 16, col*this.tileSize, row*this.tileSize, this.tileSize, this.tileSize)
        }
        else if (this.map[row][col] === 3) { // wall top
          let sx = 32
          let sy = 0  
          ctx.drawImage(spriteImg, sx, sy, 16, 16, col*this.tileSize, row*this.tileSize, this.tileSize, this.tileSize)
        }
        else if (this.map[row][col] === 5) { // wall side
          let sx = 0
          let sy = 128  
          ctx.drawImage(spriteImg, sx, sy, 16, 16, col*this.tileSize, row*this.tileSize, this.tileSize, this.tileSize)
        }
        else if (this.map[row][col] === 4) { // wall side
          let sx = 0
          let sy = 144
          ctx.drawImage(spriteImg, sx, sy, 16, 16, col*this.tileSize, row*this.tileSize, this.tileSize, this.tileSize)
        }
        else if (this.map[row][col] === 6) { // wall side
          let sx = 0
          let sy = 112
          ctx.drawImage(spriteImg, sx, sy, 16, 16, col*this.tileSize, row*this.tileSize, this.tileSize, this.tileSize)
        }
        else if (this.map[row][col] === 7) { // wall side
          let sx = 16
          let sy = 128
          ctx.drawImage(spriteImg, sx, sy, 16, 16, col*this.tileSize, row*this.tileSize, this.tileSize, this.tileSize)
        }
        else if (this.map[row][col] === 8) { // wall side
          let sx = 48
          let sy = 144
          ctx.drawImage(spriteImg, sx, sy, 16, 16, col*this.tileSize, row*this.tileSize, this.tileSize, this.tileSize)
        }
        else {
          ctx.fillStyle = "black"
          ctx.fillRect(col * this.tileSize, row * this.tileSize, this.tileSize, this.tileSize);
        }
      }
    }
  }
}

  


